#!/bin/bash

# =============================================
# 依賴套件安裝腳本
# 
# 此腳本用於互動式安裝 npm 依賴套件，功能包含：
# - 互動式輸入套件名稱
# - 輸入驗證
# - 自動執行 npm install
# 
# 使用方式：
# ./script/install.sh
# =============================================

read -p "請輸入要安裝的依賴包名稱: " pkg
if [ -z "$pkg" ]; then
  echo "未輸入依賴包名稱，操作中止。"
  exit 1
fi
npm install "$pkg"
